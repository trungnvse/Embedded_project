int main(void) {

	/* initialize variable real time from application start */
	uint32_t timer = 0;

	/* set value for time off and on*/
	led_blue_on    = 4;
	led_blue_off   = 6;
	led_green_on   = 7;
	led_green_off  = 3;

    /* initialize led and timer RSTC */
	init_Led();
	init_rstc();

    while(1) {
    	timer = get_timer();

		change_status_light_blue(timer);

		change_status_light_green(timer);
    }
    return 0 ;
}


