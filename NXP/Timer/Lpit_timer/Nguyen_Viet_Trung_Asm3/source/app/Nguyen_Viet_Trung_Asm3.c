/*******************************************************************************
* Global variable
*******************************************************************************/


/*******************************************************************************
* Code
*******************************************************************************/



int main(void)
{
	init_timer();
	init_Led();
	/* 1s equal 20 times 0.05s*/
	g_second = 20;

	/* 1s equal 47 times 0.05s*/
	g_two_thityfive = 47;

    while (1)
    {
    	/* count down variable g_second in LPIT0_IRQHandler from 20 to 0 ~ 1 second */
    	if(0 == g_second)
    	{
    		FGPIOB->PTOR |= GREEN_LED_PIN;/* toggle led after 1s */
    		g_second = 20;
    	}

    	/* count down variable g_two_thityfive in LPIT0_IRQHandler from 47 to 0 ~ 2.35s second */
    	if(0 == g_two_thityfive)
		{
			FGPIOD->PTOR |= BLUE_LED_PIN;/* toggle led after 2.35s */
			g_two_thityfive = 47;
		}
    }
}




